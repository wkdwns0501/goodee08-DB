-- - 로그 파일: 작업 내용을 저장(데이터 복구에 사용)
-- - 인덱스 파일: 빠른 검색을 위한 정보 저장(예: 책 뒤에 인덱스와 같은 개념)
-- - 설정 파일: DBMS의 구성과 운영 설정을 저장
-- - 메타데이터 파일: 데이터베이스 구조(테이블, 데이터 타입, 관계 등)에 대한 정보를 저장

