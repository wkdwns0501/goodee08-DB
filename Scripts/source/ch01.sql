-- - 열(column): 데이터의 속성(어떤 종류의 데이터인지), 예: 고객 테이블이라면 고객번호, 이름, 연락처, 주소 등
-- - 행(row): 하나의 데이터 항목, 예: 고객 테이블에서 하나의 행은 고객 한 명의 정보를 의미

